// El includes() método de Array instancias determina si un arreglo incluye un determinado valor entre sus entradas, regresando trueo false según corresponda.

// Resumen
// Comprobar si una matriz contiene el elemento especificado

const array1 = [1, 2, 3];

console.log(array1.includes(2));
// Expected output: true

const pets = ['cat', 'dog', 'bat'];

console.log(pets.includes('cat'));
// Expected output: true

console.log(pets.includes('at'));
// Expected output: false
