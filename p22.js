// El lastIndexOf() método de Array instancias devuelve el último índice en el que se puede encontrar un elemento dado en la matriz, o -1 si no está presente. La matriz se busca hacia atrás, comenzando en fromIndex.

// Resumen
// Busca en la matriz un elemento, comenzando por el final, y devuelve su posición

const animals = ['Dodo', 'Tiger', 'Penguin', 'Dodo'];

console.log(animals.lastIndexOf('Dodo'));
// Expected output: 3

console.log(animals.lastIndexOf('Tiger'));
// Expected output: 1
